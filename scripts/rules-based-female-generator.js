function validateTransformation(original, transformed, element, strength) {
  const checks = {
    hasInternalFocus: /–≤–Ω—É—Ç—Ä–µ–Ω–Ω|–ø—Ä–æ—Ü–µ—Å—Å|—Å–æ—Å—Ç–æ—è–Ω–∏–µ|–≥–ª—É–±–∏–Ω|—Å–≤—è–∑/i.test(transformed),
    hasCyclical: /—Ü–∏–∫–ª|—Ä–∏—Ç–º|–≤—Ä–∞—â|–ø–µ—Ä–µ–ª–∏–≤/i.test(transformed),
    hasCooperation: /—Å–æ—Ç—Ä—É–¥–Ω–∏—á–µ—Å—Ç–≤|–≥–∞—Ä–º–æ–Ω–∏|–ø—Ä–∏—Ç—è–∂|–ø—Ä–∏–≤–ª–µ—á/i.test(transformed),
    preservesMeaning: transformed.length > original.length * 0.7, // –°–æ—Ö—Ä–∞–Ω–∏–ª—Å—è —Å–º—ã—Å–ª
    differentEnough: transformed !== original // –ù–µ –ø—Ä–æ—Å—Ç–æ –∫–æ–ø–∏—è
  };
  
  const score = Object.values(checks).filter(Boolean).length;
  return { checks, score, passed: score >= 3 };
}

