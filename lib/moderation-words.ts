/**
 * Добавить новые запрещенные слова в базовый список
 * ВАЖНО: Изменения не сохраняются между сеансами, для постоянного сохранения редактируйте FORBIDDEN_WORDS_BASE
 */
export function addForbiddenWord(word: string, strict: boolean = false): void {
  const words = strict ? FORBIDDEN_WORDS_STRICT : FORBIDDEN_WORDS_BASE;
  if (!words.includes(word.toLowerCase())) {
    words.push(word.toLowerCase());
  }
}

/**
 * Проверить, является ли слово запрещенным
 */
export function isForbiddenWord(word: string, strict: boolean = false): boolean {
  const words = strict ? FORBIDDEN_WORDS_STRICT : FORBIDDEN_WORDS_BASE;
  return words.includes(word.toLowerCase());
}
