// –ü–û–õ–£–ß–ï–ù–ò–ï –í–°–ï–• –°–ò–ú–í–û–õ–û–í –î–õ–Ø LEVEL 2 (–¥–ª—è –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –±–µ–∑ –ë–∞—Ü–∑—ã)
export function getAllSymbolsForLevel2(gender?: 'male' | 'female'): AmuletSymbol[] {
  const allSymbols: AmuletSymbol[] = [];
  const seenIds = new Set<string>();
  
  // –°–æ–±–∏—Ä–∞–µ–º –≤—Å–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã –∏–∑ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏
  SYMBOL_LIBRARY.forEach(symbolClass => {
    symbolClass.symbols.forEach(symbol => {
      if (!seenIds.has(symbol.id)) {
        // –§–∏–ª—å—Ç—Ä—É–µ–º –ø–æ –≥–µ–Ω–¥–µ—Ä—É: –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–∏–º–≤–æ–ª—ã, –ø–æ–¥—Ö–æ–¥—è—â–∏–µ –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –≥–µ–Ω–¥–µ—Ä–∞ –∏–ª–∏ —É–Ω–∏—Å–µ–∫—Å
        if (!gender || symbol.gender.includes(gender) || symbol.gender.includes('unisex')) {
          allSymbols.push(symbol);
          seenIds.add(symbol.id);
        }
      }
    });
  });
  
  // –î–æ–±–∞–≤–ª—è–µ–º —Å–∏–º–≤–æ–ª—ã —É—Ä–æ–≤–Ω—è 1
  LEVEL1_ADDITIONAL_SYMBOLS.forEach(symbol => {
    if (!seenIds.has(symbol.id)) {
      // –°–∏–º–≤–æ–ª—ã —É—Ä–æ–≤–Ω—è 1 –æ–±—ã—á–Ω–æ unisex
      if (!gender || symbol.gender.includes('unisex') || symbol.gender.includes(gender)) {
        allSymbols.push(symbol);
        seenIds.add(symbol.id);
      }
    }
  });
  
  return allSymbols;
}

